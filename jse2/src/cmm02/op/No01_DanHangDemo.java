package cmm02.op;

public class No01_DanHangDemo {
	public static void main(String[] args) {
		//int i; 변수의 선언
		//i = 5; 초기화, 할당	
		int i = 5;
		i = i + 1;
		System.out.println("i = i + 5의값: " + i);
		
		int j = 5;
		j++;
		System.out.println("j++ 의값"+j);
		
		int k = 5;
		++k;
		System.out.println("k++의 값" + k);
	}
}
